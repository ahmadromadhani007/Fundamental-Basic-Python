#Operator relasi (Pembanding) ini digunakan untuk membandingkan 2 nilai yang menghasilkan Booelan (True / False)

# == (sama dengan)
# != (tidak sama dengan)
# > (lebih besar dari)
# < (lebih kecil dari)
# >= (lebih besar sama dengan)
# <= (lebih kecil sama dengan)

x = 10
y = 5

print(x != 10)
print(x != y)

print("Apakah x lebih besar daripada y ???", x > y)
print("Apakah x lebih kecil daripada y ???", x < y)
print("Apakah x lebih besar  sama dengan daripada y ???", x >= y)
print("Apakah x lebih kecil sama dengan daripada y ???", x <= y)