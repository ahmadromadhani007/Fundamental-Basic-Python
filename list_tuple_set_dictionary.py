# Adalah sebuah tipe data yang bisa menampung banyak nilai dalam satu variable
"""Contoh : x = [1, 2, "a", "b", True, False]"""
#Pengertian nya dari 4 tipe data tersebut :

#List 
""" 
> yaitu kumpulan data terurut (ordered sequence) yang mana bisa dirubah dan bisa di duplikat.
> List dibuat dengan kurung siku []dan setiap elemen dipisah dengan , [,]
> setiap elemen memiliki no.urut (index) yang di mulai dari 0, 1, 2 dst
"""
#Tuple
"""
> tuple hampir sama halnya dengan list (terurut,boleh duplikat dan punya index ) tapi elemennya tidak bisa dirubah
> tuple dibuat dengan kurung dan setiap elemen dipisah dengan koma,
"""
#Set
"""
> set berbeda dengan list dan tuple, konsep nya tidak terurut dan tidak punya index
> set bersifat unik / tidak bisa duplikat dan tidak bisa dirubah
> set dibuat dengan kurung kurawal dan setiap elemen dipisah dengan koma
"""
#Dictionary
"""
adalah data yang disimpan dalam bentuk pasangan kunci nilai (pair of key value) yang bersifat tidak beraturan.
> berikut tipe dari dictionary :
> menggunakan kurung kurawal
> setiap pasangan elemen dipisah dengan koma
> key dan value di pisah dengan titik dua
"""