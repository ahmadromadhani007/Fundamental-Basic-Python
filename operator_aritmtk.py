# Operator digunakan untuk melakukan operasi pada variable nilai

#Operator Aritmatika digunakan untuk melakukan operasi matematika dasar
x = 10
y = 2

# Pertambahan
hasil = x + y 
print("Hasil dari x + y adalah ", hasil)

# Pengurangan
hasil = x - y 
print("Hasil dari x - y adalah ", hasil)

# Perkalian
hasil = x * y 
print("Hasil dari x x y adalah ", hasil)

# Pembagian
hasil = x / y 
print("Hasil dari x / y adalah ", hasil)

# Modulus atau sisa bagi
hasil = x % y 
print("Hasil dari x % y adalah ", hasil)

# Floor devision
hasil = x // y 
print("Hasil dari x // y adalah ", hasil)

# Exsponen atau pangkat
hasil = x ** y 
print("Hasil dari x ** y adalah ", hasil)

# Negasi
print(-x)

# Prioritas di dalam operator aritmatika yaitu :
# Tanda Kurung ()
# ** eksponen atau pangkat
# *,/,//,% perkalian,pembagian,floor division dan modulus
# Tambah dan kurang + -

print((2 + 4**2) / 2)